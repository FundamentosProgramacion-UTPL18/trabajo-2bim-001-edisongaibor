
package arreglo1;

public class Arreglo1 {

    public static void main(String[] args) {
        //Arreglos
        int[]c=new int[10];
        //En la primera posicion del arreglo se guarda el valor 10
        c[0]=10;
        for(int contador=0;contador<c.length;contador++){
            System.out.println(c[contador]);
            
        }
        
    }
    
}
